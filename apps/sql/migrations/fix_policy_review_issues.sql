시행일: 2026년 4월 21일

본 정책은 모두의수선(이하 "회사")이 제공하는 비대면 의류 수선 서비스의 결제, 주문 취소 및 환불에 관한 사항을 규정합니다.


제1조 (운영시간 안내)
환불·취소 처리는 회사의 영업시간 내에 진행됩니다.
- 영업일: 평일 (월요일 ~ 금요일)
- 영업시간: 오전 10시 ~ 오후 6시
- 휴무일: 주말, 법정공휴일, 임시공휴일, 연휴 전체
영업시간 외 또는 휴무일에 접수된 환불·취소 요청은 다음 영업일 순차적으로 처리됩니다.


제2조 (결제수단)
1. 회사는 토스페이먼츠를 통해 다음의 결제수단을 제공합니다.
   - 신용카드 / 체크카드
   - 계좌이체 / 가상계좌
   - 간편결제 (토스페이, 카카오페이, 네이버페이 등)
   - 휴대폰 소액결제
2. 결제수단별 한도 및 정책은 각 결제수단 제공사의 정책을 따릅니다.
3. 모든 결제는 결제수단 제공사의 보안 절차에 따라 안전하게 처리되며, 회사는 카드번호 등 결제 인증 정보를 직접 저장하지 않습니다.


제3조 (결제 시점 및 금액 확정)
1. 수선 신청 시 견적 금액에 대해 결제가 진행됩니다.
2. 의류의 상태나 추가 작업 필요 사항이 확인된 경우, 작업 시작 전 추가 결제를 요청드릴 수 있으며, 고객 동의 후 진행됩니다.
3. 추가 결제에 동의하지 않으시는 경우 작업은 진행되지 않으며, 의류는 회수 시 사용한 배송비를 제외하고 반송됩니다.


제4조 (주문 취소)
1. 작업 시작 전 취소
   - 수거 전 또는 작업 시작 전까지는 언제든지 취소가 가능하며, 결제하신 금액 전액이 환불됩니다.
   - 취소는 앱/웹의 주문 상세 화면 또는 고객센터(영업시간 내)를 통해 요청하실 수 있습니다.
2. 작업 시작 후 취소
   - 수선 작업이 시작된 이후에는 취소 및 환불이 불가합니다.
   - 단, 회사의 귀체사유(작업 불량, 의룰 내용과 다른 작업 등)에 해당하는 경우 제6조에 따라 처리됩니다.
3. 작업 완료 후 취소
   - 수선 작업이 완료된 경우 단순 변심에 의한 취소는 불가합니다.
   - 단, 회사의 귀체사유(작업 불량, 의룰 내용과 다른 작업 등)에 해당하는 경우 제6조에 따라 처리됩니다.


제5조 (환불 처리 절차 및 소요 기간)
1. 환불은 원칙적으로 결제하신 동일한 결제수단으로 진행됩니다.
2. 환불 처리 기간 (회사 영업일 기준)
   - 신용 / 체크카드: 카드사 영업일 기준 3 ~ 7일 (카드사 정책에 따라 차이 있을 수 있음)
   - 계좌이체 / 가상계좌: 영업일 기준 1 ~ 3일 (입력하신 환불 계좌로 입금)
   - 간편결제: 각 결제사 정책에 따라 영업일 기준 3 ~ 5일
3. 환불 신청 시 다음 정보가 필요할 수 있습니다.
   - 주문 번호
   - 환불 사유
   - (계좌 환불 시) 예금주, 은행명, 계좌번호


제6조 (회사 귀체에 의한 환불 및 보상)
1. 다음의 경우 회사 귀체로 보아 전액 환불 또는 재작업을 진행합니다.
   - 의룰하신 수선 내용과 다르게 작업된 경우
   - 작업 과정에서 의류에 새로운 손상이 발생한 경우
   - 회사의 사정으로 작업이 불가능하게 된 경우
2. 보상 한도
   - 의류 손상 시 보상 한도는 해당 의류의 객관적 잔존 가치(실거래가, 구매가, 사용 기간 등을 고려한 시세액)를 기준으로 산정합니다.
   - 리셀러 시세, 프리미엄 가격, 한정판 휘소성 등에 의한 부가가치는 보상 산정 기준에서 제외됩니다.
   - 명품, 한정판 등 고가 의류는 신청 시 사전에 가치를 신고해 주셔야 적정한 보상이 가능하며, 미신고 시 객관적 잔존 가치 범위 내에서 보상됩니다.
3. 보상 청구는 의류 수령일로부터 7일 이내에 사진 등 증빙과 함께 고객센터로 요청해 주시기 바랍니다.


제7조 (수거/배송 관련 환불)
1. 수거 후 작업 불가 판정 시
   - 의류 상태 등의 이유로 작업이 불가능한 경우, 수선 비용은 전액 환불됩니다.
   - 반송 배송비(실것 제공 비용)는 고객 부담입니다. 의류 상태에 의한 작업 불가는 회사의 귀체사유가 아니므로 수거 및 반송에 사용된 실협 배송비는 더하지 않습니다.
2. 고객 부재 등으로 수거가 실패한 경우
   - 수거 시도 횟수 초과 등으로 수거가 최종 실패한 경우, 수선 비용은 환불되며 사용된 배송비는 차감될 수 있습니다.
3. 배송 중 분실 / 파손
   - 회사가 지정한 배송사를 통한 배송 중 분실 또는 파손이 발생한 경우, 회사가 체임지고 보상합니다.


제8조 (가상계좌 결제의 미입금)
1. 가상계좌로 결제하신 경우 입금 기한 내에 입금하지 않으시면 주문이 자동 취소됩니다.
2. 입금 기한이 만료된 가상계좌는 사용할 수 없으며, 재결제가 필요합니다.


제9조 (분쟁 해결)
1. 환불 및 보상에 관한 분쟁이 발생한 경우, 양 당사자는 상호 협의하여 해결하는 것을 원칙으로 합니다.
2. 협의가 이루어지지 않을 경우 다음 기관을 통해 분쟁 조정을 요청하실 수 있습니다.
   - 한국소비자원 (☎ 1372)
   - 한국공정거래조정원 (https://www.kofair.or.kr)
3. 본 정책에 명시되지 않은 사항은 「전자상거래 등에서의 소비자보호에 관한 법률」, 「소비자기본법」 등 관련 법령 및 회사의 이용약관을 따릅니다.


제10조 (고객센터 운영시간)
환불 및 결제 관련 문의는 영업시간(평일 오전 10시 ~ 오후 6시) 내에 응대됩니다.
자세한 회사 정보 및 연락처는 사이트 하단의 회사 정보를 참고해 주시기 바랍니다.


